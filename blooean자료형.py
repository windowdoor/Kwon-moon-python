#참 / 거짓
print(5 > 10)#print는 출력을 위한 명령문이고 5는 10보다 작은수 이기 때문에 false가 출력된다. 
print(5 < 10)#print는 출력을 위한 명령문이고 10은 5보다 큰수 이기 때문에 true가 출력된다.
print(True)#print 출력을 위한 명령문이고 true 을 출력한다.
print(False)#print 출력을 위한 명령문이고 false 출력한다.
print(not True)#print 출력을 위한 명령문이고 (not True)이기 때문에 false를 출력한다.
print(not False)#print 출력을 위한 명령문이고 (nor false)이기 떄문에 true를 출력한다.
print(not(5 > 10))#print 출력을 위한 명령문이고 (not (false))이기 때문에 true를 출력한다.
print(not(5 < 10))#print 출력을 위한 명령문이고(not(true))이기 때문에 false 출력한다.
